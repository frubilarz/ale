    Integer mesStringToInteger(String mes) {
        if ("Enero".equals(mes)) {
            return 1;
        }
        if ("Febrero".equals(mes)) {
            return 2;
        }
        if ("Marzo".equals(mes)) {
            return 3;
        }
        if ("Abril".equals(mes)) {
            return 4;
        }
        if ("Mayo".equals(mes)) {
            return 5;
        }
        if ("Junio".equals(mes)) {
            return 6;
        }
        if ("Julio".equals(mes)) {
            return 7;
        }
        if ("Agosto".equals(mes)) {
            return 8;
        }
        if ("Octubre".equals(mes)) {
            return 10;
        }
        if ("Septiembre".equals(mes)) {
            return 9;
        }
        if ("Noviembre".equals(mes)) {
            return 11;
        }
        if ("Diciembre".equals(mes)) {
            return 12;
        }
        return 0;
    }

