    private void BuscarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarBotonActionPerformed
        inciarJtable();
        String mesString = this.mes_text1.getSelectedItem().toString();
        Integer mesInteger = mesStringToInteger(mesString);
        Integer anio = NumberUtils.NumberUtils(this.anio_text.getSelectedItem().toString());
        if (mesInteger != null && anio != null) {
            Integer dia = 1;
            Date fecha1 = FechaUtils.getFecha(anio, mesInteger - 1, dia);
            Date fecha2 = FechaUtils.getFecha(anio, mesInteger, dia - 1);
            ServicioDB servicio = new ServicioDB();
            List<Boleta> boletas = servicio.getBoletas(fecha1, fecha2);
            for (Boleta boleta : boletas) {
                Usuario usuario = servicio.getUsuarioPorId(boleta.getUsuario_id());
                Object fila[] = {usuario.getNombre(), boleta.getMonto()};
                defaultTableModel.addRow(fila);
            }

        }
    }//GEN-LAST:event_BuscarBotonActionPerformed

